    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean bandera = true;
        for (Utensilios f : listaUten){
            if(f.getNombre().equals(nombre.getText())){
                f.addStock(Integer.parseInt(cantidad.getText()));
                f.setCosto(Double.parseDouble(precio.getText()));
                letrero.setText("entramos");
                bandera = false;
            }   
        } 
        if (bandera == true){
            Utensilios ute = new Utensilios(); 
            ute.addStock(Integer.parseInt(cantidad.getText()));
            ute.setNombre(nombre.getText());
            ute.setCosto(Double.parseDouble(precio.getText()));
            listaUten.add(ute);
            letrero.setText(ute.getNombre());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

